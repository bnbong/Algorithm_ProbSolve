# 1 : 1 -> 1 개
# 2 : 1+1 / 2 -> 2개
# 3 : 1+1+1 / 2+1 / 1+2 -> 3개
# 4 : 1+1+1+1 / 2+1+1 / 1+2+1 / 1+1+2 / 2+2 -> 5개
# 5 : 1+1+1+1+1 / 2+1+1+1 / 1+2+1+1 / 1+1+2+1 / 1+1+1+2 / 2+2+1 / 2+1+2 / 1+2+2 -> 8개
n = int(input())

t = [0]
t[0] = 1
t.append(2)
t.append(3)

if n == 1:
    print(t[0] % 10007)  # 1 가지 경우
elif n == 2:
    print(t[1] % 10007)
elif n == 3:
    print(t[2] % 10007)
else:
    for i in range(3, n):
        t.append(t[i-2] + t[i-1])
    print(t[n-1] % 10007)

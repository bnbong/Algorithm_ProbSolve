#                 2b+1+1+1 / 1+2b+1+1 / 1+1+2b+1 / 1+1+1+2b / 2a+2b+1 / 2b+2a+1 / 2a+1+2b / 2b+1+2a / 1+2a+2b / 1+2b+2a
#                 2b+2b+1 / 2b+1+2b / 1+2b+2b-> 21개
n = int(input())

t = [0]
t[0] = 1
t.append(3)
t.append(5)

if n == 1:
    print(t[0] % 10007)  # 1 가지 경우
elif n == 2:
    print(t[1] % 10007)
elif n == 3:
    print(t[2] % 10007)
else:
    for i in range(3, n):
        t.append(t[i-2] * 2 + t[i-1])
    print(t[n-1] % 10007)
